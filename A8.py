# ["Bob", 79], ["Tom", 59], ["Ken", 61]という3つの要素をもつ変数「users_info」というリストを定義。
users_info = [["Bob", 79], ["Tom", 59], ["Ken", 61]]

# 変数「users_info」のインデント番号「0」の中のインデント番号「0」と「1」を出力。
print(f"Name: {users_info[0][0]}, Age: {users_info[0][1]}")

# 変数「users_info」のインデント番号「1」の中のインデント番号「0」と「1」を出力。
print(f"Name: {users_info[1][0]}, Age: {users_info[1][1]}")

# 変数「users_info」のインデント番号「2」の中のインデント番号「0」と「1」を出力。
print(f"Name: {users_info[2][0]}, Age: {users_info[2][1]}")
