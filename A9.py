# {"first_name": "Bob", "family_name": "Dylan", "age": 79}という3つの要素をもつ変数「bob_info」という辞書を定義。
bob_info = {"first_name": "Bob", "family_name": "Dylan", "age": 79}

# 変数「bob_info」の「"first_name"」というキーがもつバリューを出力。
print(bob_info["first_name"])  # "Bob"

# 変数「bob_info」の「"family_name"」というキーがもつバリューを出力。
print(bob_info["family_name"])  # "Dylan"

# 変数「bob_info」の「"age"」というキーがもつバリューを出力。
print(bob_info["age"])  # 79
